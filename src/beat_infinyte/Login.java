    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed
        PreparedStatement st;
        ResultSet rs;
        
        // get the username & password
        String username = UsernameField.getText();
        String password = String.valueOf(PasswordField.getPassword());
        
        //create a select query to check if the username and the password exist in the database
        String query = "SELECT * FROM `Usuario` WHERE `username` = ? AND `password` = ?";
        
        // Mostrar al usuario mensajes de campos vacios
        if(username.trim().equals("username"))
        {
            JOptionPane.showMessageDialog(null, "Introduce tu usuario", "Usuario vacio", 2);
        }
        else if(password.trim().equals("password"))
        {
            JOptionPane.showMessageDialog(null, "Introduce tu contrase침a", "Contrase침a vacia", 2);
        }
        else{
            
            try {
            st = Conexion.conexion.prepareStatement(query);
            
            st.setString(1, username);
            st.setString(2, password);
            rs = st.executeQuery();
            
            
            if(rs.next())
            {

                //abrir el MainMenu
                close();
                MainMenu menu = new MainMenu(rs.getInt("id_user"));
                menu.setVisible(true);
                
            }else{
                // mensaje de error
                JOptionPane.showMessageDialog(null, "Usuario / Contrase침a inv치lidos","Login Error",2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        }
        
    }//GEN-LAST:event_LoginButtonActionPerformed

